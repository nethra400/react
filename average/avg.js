var marks = [80, 77, 88];
var avg1 = (marks[0] + marks[1] + marks[2]) / marks.length;

console.log(avg1);
switch (avg1) {
  case avg1:
    console.log("A Grade");
    break;
  case 80:
    console.log("B Grade");
    break;
  case 70:
    console.log("C Grade");
    break;
    default:
        console.log("unavailable")
}
